  <p class="lead">نـشأة الـوزارة وتطو​رها
تأسست وزارة الشؤون الاجتماعية في عام 1951م والتي ضمت قسماً خاصاً للعمال ليقوم بالإشراف على التنظيم النقابي، وفي عام 1960م تأسست أول دائرة للعمل تنفيذاً لأحكام قانون العمل رقم (21) لسنة 1960م حيث أضيفت تسمية العمل إلى وزارة الشؤون الاجتماعية، وتقديراً من الحكومة للدور الكبير الذي تقوم به القوى العاملة في عملية التنمية الاقتصادية والاجتماعية،فقد تم إنشاء وزارة العمل كوزارة مستقلة في عام 1976م بموجب نظام تنظيمها رقم (40) لسنة 1976م.​

وتتولى الوزارة منذ إنشائها مسؤولية تحقيق الأهداف العامة لشؤون العمل والعمال في المملكة، ولمواكبة التطورات الاجتماعية والاقتصادية فقد تم إصدار قانون العمل رقم (8) لسنة 1996م ووضع نظام تنظيم إداري للوزارة رقم (38) لسنة 1994م وتعديلاته.

وتعتبر وزارة العمل من الوزارات الخدمية التي تسعى إلى تقديم الخدمات للمواطنين، ومن أبرز الخدمات التي تقوم الوزارة بتقديمها، خدمات (الإشراف على شؤون العمل والعمال، المساهمة في تنظيم سوق العمل الأردني، تشغيل الاردنيين في الداخل والخارج ...الخ).

مهام الـوزارة
 الاشراف على شؤون العمل والعمال وممارسة جميع الصلاحيات والمسؤوليات المتعلقة بتلك الشؤون والمنصوص عليها في سائر التشريعات.

 رعاية العمال الأردنيين خارج المملكة وتنمية علاقات العمل مع الدول المستقبلة لهم وتنظيم الشؤون المتعلقة بالعاملين الأجانب داخل المملكة والإشراف عليهم وتحديد شروط عملهم.

تنظيم سوق العمل الأردني ووضع التعليمات اللازمة لتوفير فرص العمل والتشغيل للأردنيين داخل المملكة وخارجها وبالتعاون مع الجهات المختصة.

تسجيل نقابات العمال ونقابات أصحاب العمل.

المساهمة والمشاركة في أعمال اللجنة الثلاثية وذلك لتمكين اللجنة من القيام بالمهام والصلاحيات المنيطة بها والواردة في نظام اللجنة الثلاثية لشؤون العمل رقم (21) لسنة 2012 وقانون العمل.

تتولى الوزارة توفير فرص العمل للأردنيين داخل المملكة وخارجها.

تنمية التعاون والتنسيق مع منظمات العمل العربية والدولية وبما يخدم قطاع العمل.

الرؤيــة
سوق عمل فاعل بعمالة وطنية مؤهلة ومنتجة في ظل بيئة عمل مستقرة وآمنة.

الرسالة
تنظيم سوق العمل وتطويره ضمن أفضل الممارسات وضمان تكافؤ الفرص من خلال استخدام عمالة وطنية مؤهلة ومنتجة وإيجاد منظومة متكاملة من المعايير والسياسات والأدوات الرقابية وفق نهج تشاركي مع المؤسسات الرسمية المعنية والقطاع الخاص والشركاء الاجتماعيين.</p>
